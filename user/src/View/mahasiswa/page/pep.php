<br>
<br>
Festival ini diikuti oleh lebih dari 50 tim mahasiswa dari berbagai program studi, yang memamerkan inovasi mereka di berbagai bidang seperti teknologi ramah lingkungan, aplikasi berbasis kecerdasan buatan (AI), hingga produk kreatif berbasis budaya lokal. Salah satu inovasi yang mencuri perhatian adalah Smart Waste Management, sebuah aplikasi berbasis IoT (Internet of Things) yang dirancang oleh mahasiswa Teknik Informatika. Aplikasi ini bertujuan untuk membantu masyarakat mengelola sampah secara lebih efektif dengan teknologi yang terintegrasi.

Selain pameran inovasi, acara ini juga diramaikan dengan berbagai kegiatan menarik seperti seminar teknologi, workshop, kompetisi pitching ide startup, dan diskusi panel yang menghadirkan pembicara dari kalangan akademisi dan industri. Topik yang dibahas meliputi perkembangan teknologi terkini, pentingnya inovasi berkelanjutan, hingga tantangan dalam mengimplementasikan teknologi di masyarakat.

Direktur Polinema, Dr. Ir. Haryanto, dalam sambutannya menyampaikan bahwa festival ini menjadi bukti nyata semangat mahasiswa Polinema dalam menjawab tantangan zaman.
“Melalui festival ini, kami ingin mahasiswa tidak hanya memahami teori di kelas, tetapi juga mampu menciptakan solusi nyata yang bermanfaat bagi masyarakat luas,” ujar beliau.
<br>
<br>
Acara ini juga mendapat dukungan penuh dari berbagai perusahaan mitra dan pemerintah daerah. Mereka melihat potensi besar dari inovasi yang dihasilkan oleh mahasiswa Polinema untuk mendukung pengembangan ekonomi lokal dan nasional. Beberapa perusahaan bahkan menyatakan minat untuk bekerja sama dalam pengembangan lebih lanjut dari beberapa inovasi mahasiswa.

Lestari, salah satu peserta dari program studi Teknik Mesin, merasa bangga dan antusias mengikuti acara ini. Ia bersama timnya memamerkan prototipe kendaraan hemat energi berbahan bakar ramah lingkungan.
“Kami sangat terinspirasi melihat inovasi dari teman-teman. Acara ini memberikan kami motivasi untuk terus berkarya dan menciptakan sesuatu yang bisa diterapkan di dunia nyata,” ujarnya.

Tidak hanya mahasiswa, pengunjung juga memberikan apresiasi atas ide-ide kreatif yang ditampilkan. Banyak dari mereka yang mengaku terinspirasi dan berharap acara serupa bisa terus diadakan di masa mendatang. Sebagai penutup, Festival Inovasi Mahasiswa 2024 membuktikan bahwa Polinema terus menjadi pelopor dalam mendorong inovasi dan kreativitas mahasiswa. Direktur Polinema menegaskan bahwa acara ini akan menjadi agenda tahunan sebagai bagian dari komitmen kampus dalam memadukan pendidikan, inovasi, dan kontribusi nyata bagi masyarakat. Dengan antusiasme yang tinggi dari peserta dan dukungan luas dari berbagai pihak, acara ini diharapkan menjadi tonggak penting dalam mempersiapkan generasi muda yang kreatif, inovatif, dan siap menghadapi tantangan global.
